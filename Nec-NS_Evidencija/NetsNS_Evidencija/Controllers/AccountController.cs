        [AllowAnonymous]
        public JsonResult GetRole()
        {
            var role = "";

            if (WebSecurity.IsAuthenticated)
            {
                if (Roles.IsUserInRole("Mannager"))
                    role = "Mannager";
                else if (Roles.IsUserInRole("Coach"))
                    role = "Coach";
                else
                    role = "Student";
            }
            return Json(role, JsonRequestBehavior.AllowGet);
        }

